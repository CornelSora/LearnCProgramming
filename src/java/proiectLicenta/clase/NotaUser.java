package proiectLicenta.clase;

public class NotaUser {
    
    private int notaAplicatie;
    private int notaProfesor;
    private int userId;
    private String denumireTest;

    public int getNotaAplicatie() {
        return notaAplicatie;
    }

    public void setNotaAplicatie(int notaAplicatie) {
        this.notaAplicatie = notaAplicatie;
    }

    public int getNotaProfesor() {
        return notaProfesor;
    }

    public void setNotaProfesor(int notaProfesor) {
        this.notaProfesor = notaProfesor;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getDenumireTest() {
        return denumireTest;
    }

    public void setDenumireTest(String denumireTest) {
        this.denumireTest = denumireTest;
    }
    
}
